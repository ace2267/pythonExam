# 기본데이타
# int 정수
# float, a=10.25
# bool, a=True
# None,  Null, a=None  

int(3.5)      # 3
2e3           # 2000.0
float("1.6")  # 1.6
float("inf")  # 무한대
float("-inf") # -무한대
bool(0)       # False. 숫자에서 0만 False임,
bool(-1)      # True
bool("False") # True
a = None      # a는 None
a is None     # a가 None 이므로 True

# 복소수 
v = 2 + 3j
v.real  # 2
v.imag  # 3  